                            Это сложно. Если говорить не о "начальном" уровне, а о "продвинутом" , то конечно в первую очередь повышается осознанность. Осознанность влияет на восприятие, потому что ты видишь не только текущее свое состояние, но и что было перед этим, и куда тебя сейчас мотанёт - следовательно можешь сразу направить себя в нужный поток, либо в разрушительный, либо в созидательный. Во-вторых, из-за осознания момента, ты не так погружен в саму ситуацию, ты можешь оценить её "со стороны" - то есть, и изнутри ситуации, и с позиции стороннего наблюдателя. Происходит примерно то расщепление восприятия, о котором Магмас писала в "параллельном восприятии", если я правильно её понял, конечно. Только это работает в бодрствовании, а не в сновидении, но кому как не нам знать, что навык закрепленный в бодрствовании до состояния привычки переходит с нами в сновидение. В-третьих - ты видишь не только прямое воздействие на тебя, но и скрытое (попытки манипулировать тобой, например). И видя такие попытки, ты сам можешь влиять (манипулировать) как на людей, так и вообще на мир (с последним чисто теоретически - на практике пока у самого не получается).<br>
