                    <div class="content">Сталкинг - красивое слово, но юмор в том, что каждый его понимает по-своему. Если говорить очень конкретно, то сталкинг - это фиксация ТС в определенном положении. Вроде каждое слово понятно, но что все это обозначает? Есть разные мнения, многие из них верные. Я предлагаю еще один взгляд на эту тему - очень упрощенный, но для начала применения методов сталкинга вполне пригодный. <br>
                        Итак, сталкинг - это фиксация ТС. ТС определяет наше восприятие. Поэтому для начала, надо научиться определять эти положения ТС, или, в нашем случае, определять свое восприятие. Это поможет дисциплинировать свое внимание, повысить свою осознанность. На это и направлен наш практ. За четыре недели у нас появится привычка отслеживать свои действия при контакте с окружающим миром. <br>
                        Зачем это нужно? <br>
                        - Затем, что во-первых, вы будете ОСОЗНАННО контактировать с внешним миром, постоянно будете "начеку" (alerted в терминах КК), <br>
                        - во-вторых, это поможет вам впоследствии делать ПЕРЕПРОСМОТР (любым из способов), <br>
                        - в-третьих, это сильно ДИСЦИПЛИНИРУЕТ, что положительно сказывается на сновиденных практиках.<br>
                        И это, конечно, не все. Практ рассчитан на начинающих участников, чтобы помочь им почувствовать "магию на кончиках пальцев" - все задания будут простые, никаких предварительных навыков не нужно, опыт не нужен - каждый может справиться. Конечно, найдутся люди, для которых этот практ не годится - пусть они проходят мимо.<br>
                        На каждой неделе будет новое задание. Если у кого-то не будет что-то получаться - не страшно, вы можете просто "удлинить" неделю на две, или на столько, сколько вам понадобится для выполнения задания.</div>
